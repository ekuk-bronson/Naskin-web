export type DictKey = keyof typeof ru;

const DICTS: Record<Locale, Record<DictKey, string>> = { ru, en };

export function getT(locale: Locale) {
  const d = DICTS[locale];
  return (key: DictKey): string => d[key] ?? key;
}

/** «3 замера» / "3 scans" */
export function pluralScans(locale: Locale, n: number): string {
  if (locale === 'en') return n === 1 ? 'scan' : 'scans';
  const mod10 = n % 10;
  const mod100 = n % 100;
  if (mod100 >= 11 && mod100 <= 14) return 'замеров';
  if (mod10 === 1) return 'замер';
  if (mod10 >= 2 && mod10 <= 4) return 'замера';
  return 'замеров';
}

